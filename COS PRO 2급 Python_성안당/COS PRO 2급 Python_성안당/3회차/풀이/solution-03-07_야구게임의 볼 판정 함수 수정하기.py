# 제한조건
# 전달되는 두 정수는 100 ~ 999 사이 정수 중 각 자리의 중복은 없는 정수입니다.
# 반환되는 리스트의 [0]번 요소에는 “스트라이크” 개수 [1]번 요소에는 “볼” 개수를 저장합니다.


## 답안
def solution(a, b):
    result = [0,0]
    for i in range(3):
        temp = b
        for k in range(3):
            if a % 10 == temp % 10:
                if i==k:
                    result[0] += 1
                else:
                    result[1] += 1
            temp //= 10
        a //= 10    # b 가 아니라 a 
    return result


a = 356
b = 356
ret = solution(a,b)
print(ret)
